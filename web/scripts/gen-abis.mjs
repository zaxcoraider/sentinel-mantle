// Reads forge build artifacts and contract deployments, emits web/lib/contracts.ts.
// Run: pnpm gen:abis  (from web/ directory)
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dir = dirname(fileURLToPath(import.meta.url));
const root = join(__dir, '../..');
const outDir = join(root, 'contracts/out');
const deployFile = join(root, 'contracts/deployments/sepolia.json');
const libDir = join(__dir, '../lib');
const outputFile = join(libDir, 'contracts.ts');

mkdirSync(libDir, { recursive: true });

const deploy = JSON.parse(readFileSync(deployFile, 'utf8'));

const names = [
  'AgentRegistry',
  'SentinelGuard',
  'ReputationOracle',
  'SafetyRules',
  'EmergencyVault',
  'MockIdentityRegistry',
];

let out = `// AUTO-GENERATED by web/scripts/gen-abis.mjs — do not edit by hand.
// Run: pnpm gen:abis
import type { Address } from 'viem';

`;

// Contracts that must be deployed from the frontend (need bytecode too)
const withBytecode = new Set(['SafetyRules']);

for (const name of names) {
  const artifact = JSON.parse(
    readFileSync(join(outDir, `${name}.sol/${name}.json`), 'utf8'),
  );
  out += `export const ${name}Abi = ${JSON.stringify(artifact.abi, null, 2)} as const;\n\n`;
  if (withBytecode.has(name)) {
    out += `export const ${name}Bytecode = '${artifact.bytecode.object}' as const;\n\n`;
  }
}

out += `// ── Deployed addresses ──────────────────────────────────────────────────────
export const DEPLOYMENTS = {
  sepolia: {
    chainId: ${deploy.chainId} as const,
    AgentRegistry: '${deploy.contracts.AgentRegistry.address}' as Address,
    SentinelGuard: '${deploy.contracts.SentinelGuard.address}' as Address,
    ReputationOracle: '${deploy.contracts.ReputationOracle.address}' as Address,
    EmergencyVault: '${deploy.contracts.EmergencyVault.address}' as Address,
    MockIdentityRegistry: '${deploy.contracts.MockIdentityRegistry.address}' as Address,
    explorerBase: 'https://explorer.sepolia.mantle.xyz',
  },
} as const;

// address(0) is the NATIVE key for MNT inside SentinelGuard.balanceOf
export const NATIVE_TOKEN = '0x0000000000000000000000000000000000000000' as Address;

// Known ERC-20 tokens on Mantle Mainnet (not yet deployed on Sepolia testnet)
export const KNOWN_TOKENS = {
  mETH:  '0xcDA86A272531e8640cD7F1a92c01839911B90bb0' as Address,
  USDY:  '0x5bE26527e817998A7206475496fDE1E68957c5A6' as Address,
  USDe:  '0x5d3a1Ff2b6BAb83b63cd9AD0787074081a52ef34' as Address,
} as const;
`;

writeFileSync(outputFile, out);
console.log('✓  contracts.ts written to web/lib/contracts.ts');
